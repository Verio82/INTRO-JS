//Declara y asigna un valor numerico a dos variables
let primerNumero = 10;
let segundoNumero = 5;
//Declara operaciones matematicas con las variables
let resultadoSuma = primerNumero + segundoNumero;
let resultadoResta = primerNumero - segundoNumero;
let resultadoMultiplicacion = primerNumero * segundoNumero;
let resultadoDivision = primerNumero / segundoNumero;
//Muestra los resultados de las operaciones
console.log(`La suma de ${primerNumero} y ${segundoNumero} es: ${resultadoSuma}`);
console.log(`La resta de ${primerNumero} y ${segundoNumero} es: ${resultadoResta}`);
console.log(`La multiplicación de ${primerNumero} y ${segundoNumero} es: ${resultadoMultiplicacion}`);
console.log(`La división de ${primerNumero} y ${segundoNumero} es: ${resultadoDivision}`);
