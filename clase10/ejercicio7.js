//Declara dos variables con valores iniciales
let numero1 = 15;
let numero2 = 25;
//Intercambia los valores de las variables
let temp = numero1;
numero1 = numero2;
numero2 = temp;
//Muestra los valores antes y después del intercambio
console.log(`Antes del intercambio: numero1 = ${temp}, numero2 = ${numero2}`);
console.log(`Después del intercambio: numero1 = ${numero1}, numero2 = ${numero2}`);
